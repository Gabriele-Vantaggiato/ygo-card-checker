function normalizeName(name) {
  return name.toLowerCase().replace(/[^a-z0-9]/g, '');
}

async function resolveCard(name) {
  const url = `${API}?name=${encodeURIComponent(name)}`;
  const res = await fetch(url);
  if (!res.ok) return null;
  const json = await res.json();
  const card = json.data?.[0];
  if (!card) return null;
  return { id: String(card.id), name: card.name };
}

async function buildBanlist(id, config) {
  const entries = {};
  const nameIndex = {};
  const unresolved = [];

  for (const [status, names] of Object.entries(config.cards)) {
    for (const name of names) {
      const card = await resolveCard(name);
      await sleep(60);
      if (!card) {
        unresolved.push({ name, status });
        nameIndex[normalizeName(name)] = status;
        continue;
      }
      entries[card.id] = status;
      nameIndex[normalizeName(card.name)] = status;
      nameIndex[normalizeName(name)] = status;
    }
  }

  return {
    id,
    effectiveDate: config.effectiveDate,
    cards: entries,
    nameIndex,
    unresolved,
  };
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

mkdirSync(OUT_DIR, { recursive: true });

for (const [id, config] of Object.entries(banlists)) {
  console.log(`Building ${id}...`);
  const result = await buildBanlist(id, config);
  const outPath = join(OUT_DIR, `${id}.json`);
  writeFileSync(outPath, JSON.stringify(result, null, 2));
  console.log(
    `  Wrote ${Object.keys(result.cards).length} cards, ${result.unresolved.length} unresolved`,
  );
}

console.log('Done.');
